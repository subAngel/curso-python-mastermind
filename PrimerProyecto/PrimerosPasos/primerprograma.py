n1 = int(input("Ingrese el primer numero: "))
n2 = int(input("Ingrese el segundo numero: "))
n3 = int(input("Ingrese el tercer numero: "))

print("El numero mas grande entre {}, {}, {} es: {}".format(n1,n2,n3,max(n1,n2,n3)))
print("El numero mas pequeno {}, {}, {} es: {}".format(n1,n2,n3,min(n1,n2,n3)))
